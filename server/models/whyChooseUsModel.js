import mongoose from 'mongoose';

const whyChooseUsSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
    trim: true
  },
  description: {
    type: String,
    required: true,
    trim: true
  },
  icon: {
    type: String,
    trim: true
  },
  order: {
    type: Number,
    default: 0
  },
  isActive: {
    type: Boolean,
    default: true
  }
}, { timestamps: true });

const WhyChooseUs = mongoose.model('WhyChooseUs', whyChooseUsSchema);
export default WhyChooseUs;
